import { useRef, useState } from "react";
import { allCocktails } from "../constants";
import { useGSAP } from "@gsap/react";
import gsap from "gsap";

const Menu = () => {
  const contentRef = useRef<HTMLDivElement | null>(null);
  const [currentIndex, setCurrentIndex] = useState<number>(0);

  const totalCocktails: number = allCocktails.length;

  const goToSlide = (index: number) => {
    const newIndex = (index + totalCocktails) % totalCocktails;

    setCurrentIndex(newIndex);
  };

  const getCocktailAt = (indexOffset: number) =>
    allCocktails[
      (currentIndex + indexOffset + totalCocktails) % totalCocktails
    ];

  const currentCocktail = getCocktailAt(0);
  const prevCocktail = getCocktailAt(-1);
  const nextCocktail = getCocktailAt(1);

  useGSAP(() => {
    const slideTimeline = gsap.timeline();

    slideTimeline
      .fromTo("#title", { opacity: 0 }, { opacity: 1, duration: 0.6, ease: "power1.inOut" })
      .fromTo(
        ".cocktail img",
        { opacity: 0, xPercent: -100 },
        { opacity: 1, xPercent: 0, duration: 0.5 },
        "<"
      )
      .fromTo(
        ".details h2, .details p",
        { opacity: 0, yPercent: 100 },
        { opacity: 1, yPercent: 0, duration: 0.6, stagger: 0.1 },
        "<"
      );
  }, [currentIndex]);

  useGSAP(() => {
    const parallaxTimeline = gsap.timeline({
      scrollTrigger: {
        trigger: "#menu",
        start: "top 80%",
        end: "top 10%",
        scrub: true,
      },
    });

    parallaxTimeline.from("#m-right-leaf", {
      xPercent: 100,
      yPercent: -100,
    });

    parallaxTimeline.from("#m-left-leaf", {
      xPercent: -100,
      yPercent: 100,
    });
  }, []);

  return (
    <section id="menu" aria-labelledby="menu-heading">
      <img
        src="/images/slider-left-leaf.png"
        alt="Left leaf"
        id="m-left-leaf"
      />
      <img
        src="/images/slider-right-leaf.png"
        alt="Right leaf"
        id="m-right-leaf"
      />

      <h2 id="menu-heading" className="sr-only">
        Cocktail Menu
      </h2>

      <nav className="cocktail-tabs" aria-label="Cocktail Navigation">
        {allCocktails.map((cocktail, index) => {
          const isActive = currentIndex === index;

          return (
            <button
              key={cocktail.name}
              className={`${
                isActive
                  ? "text-white border-white"
                  : "text-white/50 border-white/50"
              }`}
              onClick={() => goToSlide(index)}
            >
              {cocktail.name}
            </button>
          );
        })}
      </nav>

      <div className="content">
        <div className="arrows">
          <button
            className="text-left"
            onClick={() => goToSlide(currentIndex - 1)}
          >
            <span>{prevCocktail.name}</span>
            <img
              src="/images/right-arrow.png"
              alt="Right arrow"
              aria-hidden="true"
            />
          </button>
          <button
            className="text-left"
            onClick={() => goToSlide(currentIndex + 1)}
          >
            <span>{nextCocktail.name}</span>
            <img
              src="/images/left-arrow.png"
              alt="Left arrow"
              aria-hidden="true"
            />
          </button>
        </div>

        <div className="cocktail">
          <img src={currentCocktail.image} alt={currentCocktail.name} className="object-contain" />
        </div>
        <div className="recipe">
          <div ref={contentRef} className="info">
            <p>Recipe for:</p>
            <p id="title">{currentCocktail.name}</p>
          </div>
          <div className="details">
            <h2>{currentCocktail.title}</h2>
            <p>{currentCocktail.description}</p>
          </div>
        </div>
      </div>
    </section>
  );
};

export default Menu;
